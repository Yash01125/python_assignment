# 28)Differentiate between append () and extend () methods?
#
# In Python, the append() and extend() methods are used to modify lists, but they operate in different ways.
#
# 1. append() Method

lst1=[1,2,3]
lst1.append(4)
lst1.append(5)
print(lst1)

# 2. extend() Method

list2 = [1, 2, 3]
list2.extend([4, 5])
print(list2)


