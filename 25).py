# 25)What is List? How will you reverse a list?

# - A list is a built-in data type that holds an ordered collection of items.
# These items can be of any type, and lists are mutable, meaning you can modify them after creation.
# Lists are defined using square brackets [], with items separated by commas.
#
# ex=
lst1=[1,2,3,4,5]
print(lst1[::-1])